public class JKad03B2 {
	public static void main(String[] args) {
		int i = 0;
		System.out.println("現在のi の値\t" + i);
		// インクリメント
		System.out.println("i++で表示\t" + (i++));
		System.out.println("現在のi の値\t" + i);
		System.out.println("++i で表示\t" + (++i));
		System.out.println("現在のi の値\t" + i);
		// デクリメント
		System.out.println("i--で表示\t" + (i--));
		System.out.println("現在のi の値\t" + i);
		System.out.println("--i で表示\t" + (--i));
		System.out.println("現在のi の値\t" + i);
	}
}